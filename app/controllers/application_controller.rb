class ApplicationController < ActionController::Base
  protect_from_forgery with: :exception

  before_action :configure_permitted_parameters, if: :devise_controller?

  protected

  def configure_permitted_parameters
    devise_parameter_sanitizer.permit(:sign_up, keys: [:username, :first_name, :last_name, :team_name])
    devise_parameter_sanitizer.permit(:account_update, keys: [:username, :first_name, :last_name, :team_name])
  end

  def after_sign_in_path_for(*)
    if !user_signed_in?
      root_path
    else
      current_user
    end
  end

end
